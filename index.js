// Adds inputs:
function add(a,b){
  return a + b;
}
// Subtracts b from a:
function subtract(a,b){
  return a - b;
}
// Divides a by b:
function divide(a,b){
  return a / b;
}
// Multiplies inputs:
function multiply(a,b){
  return a * b;
}
// Increases input by 1:
function increment(a){
  return a++;
}
// Decreases input by 1:
function decrement(a){
  return a--;
}
